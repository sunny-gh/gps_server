uint16_t crc16(uint8_t *data, uint32_t data_size) {
	if (!data || !data_size)
		return 0;
	uint16_t crc = 0;
	uint8_t *buf = data;
	while (data_size--)
		crc = (crc >> 8) ^ crc16_table[(uint8_t) crc ^ *buf++];
	return crc;
}

//  calculate 16 bits CRC of the given length data. (для GT06)
uint16_t crc16_itu(const uint8_t* pData, int nLength) {
	uint16_t fcs = 0xffff;         //  Initialize 
	while (nLength > 0) {
		fcs = (fcs >> 8) ^ crctab16[(fcs ^ *pData) & 0xff];
		nLength--;
		pData++;
	}
	return ~fcs;         //  Negate
}

// перевод числа в строку вида "A0EF", строка требует удаления через g_free()
char* crc16_to_string(uint16_t crc16) {
	int i, len = 2;
	uint8_t *blob = (uint8_t*) &crc16;
	char *str = (char *) g_malloc0((len * 2 + 1) * sizeof(char));
	for (i = 0; i < len; i++) {
		sprintf(str + i * 2, "%02x", (uint8_t) * (blob + len - 1 - i));
	}
	return str;
}

// перевод строки вида "A0EF" в число
// если символов будет больше, то return 0xffff
uint16_t crc16_from_string(uint8_t *str) {
	if (!str)
		return 0;
	uint16_t crc16 = (uint16_t) strtoul((const char *) str, NULL, 16);
	return crc16;
}

